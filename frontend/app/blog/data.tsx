// Helper functions
export function getPostBySlug(slug: string) {
  return blogPosts.find((post) => post.slug === slug);
}

export function getRelatedPosts(relatedSlugs: string[]) {
  return relatedSlugs
    .map((slug) => getPostBySlug(slug))
    .filter((post) => post !== undefined);
}
